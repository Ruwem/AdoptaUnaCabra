	public static HashSet<String> getRazaEnums(){
	
		HashSet<String> values = new  HashSet<String>();
		for (Raza c: Raza.values()){
			values.add(c.name().toUpperCase());
		}
		return values;
	}
	public static boolean contains(String test){
		HashSet<String> values = getRazaEnums();
		return values.contains(test.toUpperCase().replaceAll(" ", "_"));
	}

